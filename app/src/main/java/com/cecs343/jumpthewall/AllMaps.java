    //screens are sorted by difficulty (level 1, 2, 3)
    //screens will be picked randomly as the player is moving
    //it'll choose mostly level 1 screens at the start
    //multiple screens will be grouped into phases
    //end of each phase will have a stairclimb
    //new phase starts and background changes
    //as the game progresses, it'll have a more likely chance of choosing a level 2 or 3 screen
    //until the game gets really hard only choosing level 3 maps
    //game will also speed up

    //0 = empty
    //1 = ground
    //2 = stationary
    //3 = floating
    //4 = shielded
    //5 = pit
    //6 = swoop

    public static int[] getMapLevel(int phaseCount, int screenCount, int mapPart, int n) {
        //on even numbers return screen that has something
        //on odd numbers return blank screen

        if (screenCount % 2 == 0) {
            if (screenCount == 0) {
                return blankScreen[3];
            }
            return blankScreen[n%3];
        }
        if (screenCount == 19) {
            return levelEnd[0];
        }
        if (phaseCount == 0) {
            if (n >= 0 && n <=7) {
                return AllMaps.level1[mapPart];
            }
            return AllMaps.level2[mapPart];
        } else if (phaseCount == 1) {
            if (n >= 0 && n <=5) {
                return AllMaps.level1[mapPart];
            }
            return AllMaps.level2[mapPart];
        } else if (phaseCount == 2) {
            if (n >= 0 && n <=2) {
                return AllMaps.level1[mapPart];
            } else if (n >= 3 && n <=8) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        } else if (phaseCount == 3) {
            if (n >= 0 && n <=1) {
                return AllMaps.level1[mapPart];
            } else if (n >= 2 && n <=7) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        } else if (phaseCount == 4) {
            if (n == 0) {
                return AllMaps.level1[mapPart];
            } else if (n >= 1 && n <=5) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        } else if (phaseCount == 5) {
            if (n == 0) {
                return AllMaps.level1[mapPart];
            } else if (n >= 1 && n <=3) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        } else if (phaseCount == 6) {
            if (n == 0) {
                return AllMaps.level1[mapPart];
            } else if (n == 1) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        } else if (phaseCount == 7) {
            if (n == 0) {
                return AllMaps.level2[mapPart];
            }
            return AllMaps.level3[mapPart];
        }
        return AllMaps.level3[mapPart];
    }

